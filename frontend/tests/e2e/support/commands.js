const APP_URL = 'http://localhost:8080'

Cypress.Commands.add('login', (email, password) => {
    cy.visit(APP_URL)

    cy.contains('.auth-title', 'Login')
    cy.get('#user-email').type(email)
    cy.get('#user-password').type(password)
    cy.get('#btn-login').click()

})